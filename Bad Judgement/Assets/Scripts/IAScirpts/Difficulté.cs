// EN ATTENTE D'un MENU POUR CHANGER LA DIFFICULTEE
    void difficulte(string niveau)
    {
        switch(niveau)
        {
            case "Baby": break;
            case "facile": break;
            case "Normal": break;
            case "Difficile": break;
            case "Infamy": break;
        }
    }
    
}
